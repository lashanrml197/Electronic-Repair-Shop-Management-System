    private void tblEmployeeDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeDetailsMouseClicked
        // TODO add your handling code here:
        
        // get the selected row
        int i = tblEmployeeDetails.getSelectedRow();
        
        // create a table model
        DefaultTableModel tblModel = (DefaultTableModel) tblEmployeeDetails.getModel();

        // add data into the j frame text boxes
        txtEmpID.setText(tblModel.getValueAt(i,0).toString());
        txtETitle.setText(tblModel.getValueAt(i,1).toString());
        txtEFirstName.setText(tblModel.getValueAt(i,2).toString());
        txtELastName.setText(tblModel.getValueAt(i,3).toString());
        txtEAddress.setText(tblModel.getValueAt(i,4).toString());
        txtEDOB.setText(tblModel.getValueAt(i,5).toString());
       
        

        
        // get relevant telephone numbers and display them in the combo box
        
        try {
                // Open connection
                Connection conn = DriverManager.getConnection(dbUrl, dbUsername, dbPassword);

                // Create prepared statement
                String sql = "SELECT Tel_No FROM Emp_TelNO WHERE Emp_ID = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, tblModel.getValueAt(i, 0).toString()); // Set parameter for CUST_ID
                ResultSet resultSet = pst.executeQuery(); // Execute prepared statement

                // Clear combo box before adding new items
                comboTeleEmployee.removeAllItems();

                // Iterate through the result set and add items to combo box
                while (resultSet.next()) {
                    String item = resultSet.getString("Tel_No");
                    comboTeleEmployee.addItem(item);
                }

                // Close resources
                resultSet.close();
                pst.close();
                conn.close();
            
                } catch (SQLException ex) {
                    Logger.getLogger(viewCustomer.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }

    }//GEN-LAST:event_tblEmployeeDetailsMouseClicked

