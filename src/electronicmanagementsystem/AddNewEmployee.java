    private void btnRegisterEmployee1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterEmployee1ActionPerformed
        // TODO add your handling code here:
        String employeeId = txtEmployeeID.getText().trim();
        String title = txtEmployeeName.getText().trim();
        String firstName = txtEmployeeName2.getText().trim();
        String lastName = txtEmployeeName1.getText().trim();
        String phoneNumbersText = txtTelephoneNumber.getText().trim();
        String address = txtEmployeeAddress.getText().trim();
        String dateOfBirth = txtDateOfBirth.getText().trim();

        String[] phoneNumbers = phoneNumbersText.split(",\\s*"); // Split phone numbers by comma and optional whitespace

        // Create an Employee object and validate data
        try {
            EmployeeClass newEmployee = new EmployeeClass(title, firstName, lastName, employeeId, address, dateOfBirth, phoneNumbers[0]);
            for (String phoneNumber : phoneNumbers) {
                if (!phoneNumber.matches("\\d{10}")) {
                    throw new IllegalArgumentException("Phone number must contain exactly 10 digits.");
                }
            }
            newEmployee.setTelephoneNumbers(Arrays.asList(phoneNumbers));
            // Add to database
            newEmployee.addToDatabase();
            JOptionPane.showMessageDialog(this, "Employee registered successfully!", "Registration Successful", JOptionPane.INFORMATION_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error adding employee to database: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterEmployee1ActionPerformed

