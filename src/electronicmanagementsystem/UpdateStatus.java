    private void btnDoneUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneUpdateActionPerformed
        // TODO add your handling code here:
        Connection conn = null;

        try {
            // Open connection
            conn = DriverManager.getConnection(dbUrl, dbUsername, dbPassword);

            // Check if record exists for the entered job ID
            String jobId = txtID.getText();
            String checkSql = "SELECT * FROM Add_New_Repair WHERE Job_NO = ?";
            java.sql.PreparedStatement checkStmt = conn.prepareStatement(checkSql);
            checkStmt.setString(1, jobId);
            ResultSet rs = checkStmt.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(rootPane, "No record found for Job ID: " + jobId, "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method as there is no record to update

            }

            // Confirm update
            int msg = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to Update Status?",
                    "Update Confirmation", JOptionPane.YES_NO_OPTION);

            // If they press yes then update the status.
            if (msg == JOptionPane.YES_OPTION) {
                // Update status
                String sql1 = "UPDATE Add_New_Repair SET Status_R = ? WHERE Job_NO = ?";
                java.sql.PreparedStatement pst1 = conn.prepareStatement(sql1);
                pst1.setString(1, "Done");
                pst1.setString(2, jobId);
                pst1.executeUpdate();
                
                conn.close();

                // Show success message
                JOptionPane.showMessageDialog(rootPane, "Status Successfully Updated", "Update Confirmation", JOptionPane.INFORMATION_MESSAGE);
                txtID.setText("");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error executing SQL query: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error executing SQL query: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDoneUpdateActionPerformed

