    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        if (("root".equals(txtUserName.getText()))&&("123abc".equals(txtPassword.getText())))
        {
            // if the password and username is correct open the next window
            home_page wid1 = new home_page();
            wid1.setVisible(true);  
            //close the currunt window
            this.setVisible(false);
            
            txtUserName.setText("");
            txtPassword.setText("");
        }
        
        //no details entered
        else if (("".equals(txtUserName.getText()))||("".equals(txtPassword.getText())))
        {
           JOptionPane.showMessageDialog(null,"Please enter Username and Password");
        }
        // display error message for an invalid user name
        else if (!"root".equals(txtUserName.getText()))
        {
            JOptionPane.showMessageDialog(null,"Invalid User Name");
        }
        //display error message for an invalid password
        else if (!"123abc".equals(txtPassword.getText()))
        {
            JOptionPane.showMessageDialog(null,"Invalid Password");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

