    private void btnJobIDSearchHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJobIDSearchHistoryActionPerformed
        // TODO add your handling code here:
        String jobID = txtJobIDSearchHistory.getText();

        String query = "SELECT CONCAT(e.EMP_FirstName, ' ', e.EMP_LastName) AS Employee_Name, "
                + "rc.Cost, r.Intake_Date AS Intake_Date, d.Drop_Off_Date "
                + "FROM Add_New_Repair r "
                + "JOIN Add_New_EMP e ON r.EMP_ID = e.EMP_ID "
                + "JOIN Repair_Cost rc ON r.Job_NO = rc.Job_NO "
                + "JOIN Customer c ON r.Cust_ID = c.Cust_ID "
                + "JOIN Repair_DropOff d ON r.Job_NO = d.Job_NO "
                + "WHERE r.Job_NO = ?";

        try (Connection conn = DriverManager.getConnection(dbUrl, dbUsername, dbPassword); PreparedStatement stmt = conn.prepareStatement(query)) {

            stmt.setString(1, jobID);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblRepairHistory.getModel();
            model.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                String employeeName = rs.getString("Employee_Name");
                double cost = rs.getDouble("Cost");
                String intakeDate = rs.getString("Intake_Date");
                String dropOffDate = rs.getString("Drop_Off_Date");

                model.addRow(new Object[]{employeeName, cost, intakeDate, dropOffDate});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error retrieving repair history: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    

    }//GEN-LAST:event_btnJobIDSearchHistoryActionPerformed

